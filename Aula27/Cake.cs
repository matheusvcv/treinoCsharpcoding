namespace Aula27;

public class Cake
{
    public string Flavor;
    public string Filling;
    public string Coverage;
    public int Layers;
    public string Size;

    public Cake(string flavor, string filling, string coverage, int layers, string size)
    {
        Flavor = flavor;
        Filling = filling;
        Coverage = coverage;
        Layers = layers;
        Size = size;
    }
}